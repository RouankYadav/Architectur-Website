    // Last name validation
    if (lastName.trim() === "") {
        alert("Please enter your last name.");
        return false;
    }

    // Email validation using regex
    const emailPattern = /^[^ ]+@[^ ]+\.[a-z]{2,6}$/;
    if (!emailPattern.test(email)) {
        alert("Please enter a valid email.");
        return false;
    }

    // Message validation
